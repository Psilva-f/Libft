void	*ft_memset(void *b, int c, size_t len)
/* A função memset () preenche os primeiros n bytes da área de memória
       apontado por s com o byte constante c .
VALOR DE RETORNO         topo
       A função memset () retorna um ponteiro para a área de memória s .*/
{
	unsigned char	*ptr;

	ptr = (unsigned char*)b;
	while (len-- > 0)
		*(ptr++) = (unsigned char)c;
	return (b);
}